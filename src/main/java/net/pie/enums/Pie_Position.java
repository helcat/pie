package net.pie.enums;

/** *******************************************************<br>
 * <b>Pie Position</b><br>
 * Settings for encoded image when size is used and the image is smaller than the area required.<br>
 * The default is "MIDDLE_CENTER".
 **/
public enum Pie_Position {
    TOP_LEFT,
    TOP_CENTER,
    TOP_RIGHT,
    MIDDLE_LEFT,
    MIDDLE_CENTER,
    MIDDLE_RIGHT,
    BOTTOM_LEFT,
    BOTTOM_CENTER,
    BOTTOM_RIGHT,
    ;

}


