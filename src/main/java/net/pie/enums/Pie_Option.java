package net.pie.enums;

/** *******************************************************<br>
 * <b>Pie Option</b><br>
 * Settings for encoding and Decoding.<br>
 * OVERWRITE_FILE : Allows files with the same name to be overwritten.<br>
 * RUN_GC_AFTER_PROCESSING : Run garbage collection after processing<br>
 * MODULATION : Makes every image unique
 * DEMO_MODE : Only used for PIE Demo
 * CREATE_CERTIFICATE : Creates a Pie Certificate for encryption
 **/

public enum Pie_Option {
    OVERWRITE_FILE,
    RUN_GC_AFTER_PROCESSING,
    MODULATION,
    DEMO_MODE,
    CREATE_CERTIFICATE
    ;

}


